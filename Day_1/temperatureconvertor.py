#Temperature convertor

#Temperature in degree celcious
temperature_In_celcious = input("Enter the Temperature in Degree Celcious >")

#Temperature in degree fahrenite
temperature_In_fahrenite = float(temperature_In_celcious*9/float(5))+32

#Temperature in Kelvin
temperature_In_kelvin = float(temperature_In_celcious)+273

#final
print("temperature_In_fahrenite:")
print(temperature_In_fahrenite)

print("temperature_In_kelvin:")
      
print(temperature_In_kelvin)
